      wager
    end
  end

  #------------------- CONDITIONALS -------------------#

  def natural?(hand)
    score(hand) == 21
  end

  def hit?
    @input == "y" || @input == ""
  end

  def bust?
    score(@player_hand) > 21
  end

  def stay?
    @input == "n"
  end

  def blackjack?
    score(@player_hand) == 21
  end

  def over?
    bust? || stay? || blackjack?
  end

  def really_over?
    @chips <= 0
  end

  #------------------- GAME PLAY -------------------#

