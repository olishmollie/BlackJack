  #------------------- SCORES AND BETTING -------------------#

  def score(hand)
    score = 0
    hand = hand.to_s.scan(/\w+/)
    hand.each do |card| 
      if card.to_i > 0
        score += card.to_i
      elsif card.to_i == 0
        if card == "A"
          score += 11
        else
          score += 10
        end
      end
    end
    if score > 21 && hand.include?("A")
      if score - 10 <= 21
        score -= 10
      elsif score - 10 > 21
        score -= (10 * hand.count("A"))
      end
    end
    score
  end

  def wager
    center_print_str("You have #{@chips} chips. Wager?")
    input = gets.strip.scan(/\d+/)[0].to_i
    if input.to_i > 0 && input.to_i <= @chips
      @wager += input.to_i
      @chips -= input.to_i
    elsif input.to_i > @chips
      delete_row(3)
      center_print_str("You don't have enough chips!")
      sleep(1)
      delete_row(3)
      wager
    else
      delete_row(3)
      center_print_str("Invalid input")
