      if card.to_i > 0
        score += card.to_i
      elsif card.to_i == 0
        if card == "A"
          score += 11
        else
          score += 10
        end
      end
    end
    if score > 21 && hand.include?("A")
      count = hand.count("A")
      until count == 0 || score <= 21
        score -= 10
        count -= 1
      end
    end
    score
  end

  def wager
    center_print_str("Wager?")
    input = gets.strip.to_i
    if input.to_i > 0 && input.to_i <= @chips
      @wager << input.to_i
    elsif input.to_i > @chips
      delete_row(3)
      center_print_str("You don't have enough chips!")
      sleep(1)
      delete_row(3)
      wager
    else
      delete_row(3)
      center_print_str("Invalid input")
      sleep(1)
      delete_row(3)
      wager
    end
  end

  #------------------- CONDITIONALS / INPUT -------------------#

  def out_of_chips?
    return true if @chips == 0
  end

  def natural?(hand)
    hand.scan(/\w+/).length == 2 && score(hand) == 21
  end

  def bust?(hand)
    score(hand) > 21
  end

  def twenty_one?(hand)
    score(hand) == 21
  end

  def can_surrender?(hand)
    @player_hand.length == 1 && hand.scan(/\w+/).length == 2
  end

  def surrender?(hand)
    @input == "x"
  end

  def over?(hand)
    bust?(hand) || twenty_one?(hand) || stay? || double?(hand) || surrender?(hand)
  end

  def done?
    @done == true
  end

  def input
    @input = gets.downcase.strip
  end

  def yes?
    @input == "y" || @input == ""
  end

  def no?
    @input == "n"
  end

  def stay?
    @input == "n"
  end

  def wager?
    @input.to_i > 0
  end

  def double?(hand)
    @input == "d"
  end

  def can_split?(hand)
    hand.scan(/\w+/).length == 2 && hand.scan(/\w+/)[0] == hand.scan(/\w+/)[1] && @player_hand.length * @wager[@h] <= @chips
  end

  def can_double?(hand)
    hand.scan(/\w+/).length == 2 && 2 * @wager[@h] <= @chips
  end

  def split?
    @input == "s"
  end

  #------------------- GAME PLAY -------------------#

  def first_deal
    @player_hand << @deck.shift
    @dealer_hand << @deck.shift
    @player_hand[0] << @deck.shift
    @dealer_hand << @deck.shift
  end
  
  def deal(hand)
    hand << @deck.shift
  end

  def player_turn(hand)
    @input = nil
    @h = @player_hand.index(hand)
    until over?(hand)
      if can_double?(hand) && can_split?(hand) && can_surrender?(hand)
        center_print_str("Hit?(Y/n) Double?(d) Split?(s) Surrender?(x)")
      elsif can_double?(hand) && can_surrender?(hand)
        center_print_str("Hit?(Y/n) Double?(d) Surrender?(x)")
      elsif can_double?(hand) && !can_surrender?(hand)
        center_print_str("Hit?(Y/n) Double?(d)")
      elsif !can_double?(hand) && can_surrender?(hand)
        center_print_str("Hit?(Y/n) Surrender?(x)")
      elsif can_double?(hand) && can_split?(hand) && !can_surrender?(hand)
        center_print_str("Hit?(Y/n) Double?(Y/n) Split?(s)")
      else
        center_print_str("Hit?(Y/n)")
      end
      input
      if yes?
        deal(hand)
        show_hands(3)
        if bust?(hand)
          center_print_str("You bust with #{score(hand)}!")
        elsif twenty_one?(hand)
          center_print_str("You have 21!!")
        end
      elsif stay?
        line_break
        center_print_str("You stay at #{score(hand)}.")
      elsif double?(hand)
        deal(hand)
        @wager[@h] *= 2
        delete_row(3)
        hold = @y
        @y = 3
        center_print_str("Wager: #{@wager}")
        @y = hold + 1
        show_hands(3)
        if bust?(hand)
          center_print_str("You bust with #{score(hand)}!")
        elsif twenty_one?(hand)
          center_print_str("You have 21!!")
        else
          center_print_str("You double your wager and stay at #{score(hand)}.") 
        end
      elsif split?
        @wager << @wager[@h]
        delete_row(3)
        hold = @y
        @y = 3
        center_print_str("Wager: #{@wager}")
        @y = hold + 1
        split = [@player_hand[@h].scan(/\[\w+\X\]/)[0], @player_hand[@h].scan(/\[\w+\X\]/)[1]]
        @player_hand[@h] = split[0]
        @player_hand << split[1]
        @player_hand.each do |hand|
          deal(hand) if hand.scan(/\w+\X/).length == 1
        end
        show_hands('split')
        @player_hand.each do |hand|
          player_turn(hand)
        end
        break
      elsif surrender?(hand)
        line_break
        center_print_str("You surrender!")
      else
        invalid_input
      end
    end
  end

  def dealer_turn
    sleep(1)
    if score(@dealer_hand) < 17
      center_print_str("Dealer hits.")
      deal(@dealer_hand)
      show_hands(4)
      dealer_turn
    elsif bust?(@dealer_hand)
      center_print_str("Dealer busts with #{score(@dealer_hand)}!")
    elsif twenty_one?(@dealer_hand)
      center_print_str("Dealer has 21!!")
    else
      center_print_str("Dealer stays at #{score(@dealer_hand)}.")
    end
  end

  def play
    until done?
      first_deal
      right_print_str("Chips: #{@chips}")
      center_print_str("Welcome to BlackJack!!!")
      center_print_str("\u207b\u207b\u207b\u207b\u207b\u207b\u207b\u207b\u207b\u207b\u207b\u207b\u207b\u207b\u207b\u207b\u207b\u207b\u207b\u207b\u207b\u207b\u207b\u207b\u207b")
      wager
      delete_row(3)
      center_print_str("Wager: #{@wager}")
      show_hands(1)
      if natural?(@player_hand[@h]) || natural?(@dealer_hand)
        nil
      else
        player_turn(@player_hand[0])
        unless @player_hand.count{ |hand| bust?(hand) } == @player_hand.length || surrender?(@player_hand[0])
          show_hands(4)
          dealer_turn
        end
      end
      line_break
      endgame
      line_break
      play_again
    end
  end

  def endgame
    winnings = 0
    losses = 0
    sleep(1)
    if natural?(@player_hand[0]) || natural?(@dealer_hand)
        if score(@player_hand[0]) == 21 && score(@dealer_hand) == 21
          center_print_str("You and the dealer both have BlackJack!!")
          show_hands(4)
        elsif score(@dealer_hand) == 21
          center_print_str("Dealer has BlackJack!!")
          losses += @wager[0]
          show_hands(4)
        elsif score(@player_hand[@h]) == 21
          center_print_str ("You have BlackJack!!")
          @wager[0] += @wager[0] / 2
          winnings += @wager[0]
          line_break
        end
    elsif surrender?(@player_hand[0])
      losses += @wager[0] / 2
    else 
      @player_hand.each_index do |i|
        if bust?(@dealer_hand)
          if score(@player_hand[i]) <= 21
            winnings += @wager[i]
          else
            losses += @wager[i]
          end
        elsif !bust?(@dealer_hand)
          if !bust?(@player_hand[i]) && score(@player_hand[i]) > score(@dealer_hand)
            winnings += @wager[i]
          elsif bust?(@player_hand[i]) || score(@player_hand[i]) < score(@dealer_hand)
            losses += @wager[i]
          end
        end
      end
    end
    net = winnings - losses
    if net > 0
      center_print_str("You win #{net} chips!")
    elsif net < 0
      center_print_str("You lost #{net.abs} chips!")
    else
      center_print_str("It's a push!")
    end
    @chips += net
  end

  def play_again
    @input = nil
    hold = @y
    @y = 1
    right_print_str("   Chips: #{@chips}")
    @y = hold
    if out_of_chips?
      center_print_str("You're out of chips. Better luck next time!")
      sleep(1)
      @done = true
      system("clear")
    else
      @wager = 0
      center_print_str("Play again?(Y/n)")
      input
      if yes?
        reset
      elsif no?
        line_break
        (@chips > 500) ? center_print_str("You won #{@chips - 500} chips! I'll see ya.") : center_print_str("You lost #{500 - @chips} chips! I'll see ya.")
        sleep(1)
        @done = true
        system("clear")
      else
        invalid_input
        play_again
      end
    end
  end
end