  end

  def clear_board
    index = 1
    while index < 24
      @board[index].fill(" ", 1..90)
      index += 1
    end
  end

  def delete_row(y)
    @board[y].fill(" ", 1..90)
    @y -= 1
  end

  def center_print_str(str)
    start = (@linewidth / 2) - (str.length / 2)
    index = 0
    str.length.times do |char| 
      @board[@y][start] = str[index]
      index += 1
      start += 1
    end
    @y += 1
    display_board
  end

  def left_print_str(str)
    grid_index = 3
    str_index = 0
    str.length.times do |char| 
      @board[@y][grid_index] = str[str_index]
      grid_index += 1
      str_index += 1
    end
  end

  def right_print_str(str)
    grid_index = -4
    str_index = -1
    str.length.times do |char| 
      @board[@y][grid_index] = str[str_index]
      grid_index -= 1
      str_index -= 1
    end
  end

  def show_hands(turn)
    if turn == 1
